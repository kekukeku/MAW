"""v2 dispatcher — adapter interface, mock adapter, dispatch result tracking."""

import subprocess
import time
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any, Optional


@dataclass
class DispatchResult:
    success: bool
    error: Optional[str] = None
    output: Optional[str] = None
    duration_seconds: float = 0.0
    invocation_id: Optional[str] = None
    is_async: bool = False




# ---------------------------------------------------------------------------
# Abstract adapter interface
# ---------------------------------------------------------------------------

class Adapter:
    """Base adapter. Subclass for each real agent."""

    agent_id: str = "unknown"

    def invoke(
        self,
        role: str,
        seat: str,
        target_path: str,
        instruction: str,
        expected_output: str,
        timeout: int = 600,
    ) -> DispatchResult:
        raise NotImplementedError


