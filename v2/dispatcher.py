# ---------------------------------------------------------------------------

def dispatch(
    item,
    wf_dir: Path,
    instruction: str,
    timeout: int = 600,
) -> DispatchResult:
    """Dispatch work to the appropriate adapter for the given agent."""
    adapter = get_adapter(item.agent)
    if adapter is None:
        return DispatchResult(success=False, error=f"No adapter for agent '{item.agent}'")

    from v2.schema import (
        proposal_path,
        comment_path,
        walkthrough_path,
        review_path,
        ARTIFACT_CHAIR_BRIEF,
        ARTIFACT_FINAL_PLAN,
        ARTIFACT_COMMIT,
        ARTIFACT_COMPLETION,
        ARTIFACT_QUESTIONS,
    )

    # Determine expected output path
    if item.role == "chair" and item.phase == "clarify":
        expected = str(wf_dir / ARTIFACT_CHAIR_BRIEF)
    elif item.role == "planner" and item.phase == "proposal":
        expected = str(wf_dir / proposal_path(item.seat))
    elif item.role == "planner" and item.phase == "comment":
        # Parse reviewer_target from the dispatch key: wf_id:comment:planner_a_on_planner_b:1
        key_parts = item.key.split(":")
        if len(key_parts) >= 3:
            seat_pair = key_parts[2]
            parts = seat_pair.split("_on_")
            if len(parts) == 2:
                expected = str(wf_dir / comment_path(parts[0], parts[1]))
            else:
                return DispatchResult(success=False, error=f"Invalid comment key: {item.key}")
        else:
            return DispatchResult(success=False, error=f"Invalid comment key: {item.key}")
    elif item.role == "chair" and item.phase == "synthesis":
        expected = str(wf_dir / ARTIFACT_FINAL_PLAN)
    elif item.role == "executor" and item.phase == "execution":
        expected = str(wf_dir / walkthrough_path(item.iteration))
    elif item.role == "reviewer" and item.phase == "review":
        expected = str(wf_dir / review_path(item.iteration))
    elif item.role == "executor" and item.phase == "commit":
        expected = str(wf_dir / ARTIFACT_COMMIT)
    elif item.role == "chair" and item.phase == "final_check":
        expected = str(wf_dir / ARTIFACT_COMPLETION)
    else:
        return DispatchResult(success=False, error=f"Unknown dispatch: role={item.role} phase={item.phase}")

    # Build target path from manifest for adapter
    manifest_path = wf_dir / "manifest.json"
    import json
    with open(manifest_path) as f:
        manifest = json.load(f)
    target_path = manifest.get("target_path", str(wf_dir))

    return adapter.invoke(
        role=item.role,
        seat=item.seat,
        target_path=target_path,
        instruction=instruction,
        expected_output=expected,
        timeout=timeout,
    )


